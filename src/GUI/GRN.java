    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed

        String supplierid = jTextField1.getText();
// validation starts
        if (supplierid.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Supplier details should be added", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String supName = jLabel27.getText();

        if (supName == null) {
            JOptionPane.showMessageDialog(this, "Invalid supplier Id", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int rowCount = jTable1.getRowCount();

        if (rowCount == 0) {
            JOptionPane.showMessageDialog(this, "Empty table", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String balance = jLabel23.getText();
        if (balance.equals("0.00") || (jLabel23.getForeground() == Color.red) || (Double.parseDouble(balance) < 0)) {
            JOptionPane.showMessageDialog(this, "Invalid balance", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }
        // validation ends

        String payment = jTextField7.getText();
        int discount = jSpinField1.getValue();

        Date d = new Date();
        String s = "YYYY-MM-dd";
        SimpleDateFormat sdf = new SimpleDateFormat(s);
        String date = sdf.format(d);

        int grn = 1;

        try {

            db.iud("INSERT INTO grn(supplier_idsupplier,datetime,payment,discount) VALUES('" + supplierid + "','" + date + "','" + payment + "','" + discount + "')");
            ResultSet search = db.search("SELECT MAX(idgrn)FROM grn");
            if (search.next()) {
                grn = search.getInt(1);
            }
        } catch (Exception e) {

            ErrorReporter.reportError(e, this, ErrorReporter.GENERIC_ERROR_MESSAGE);
            return;
        }
        double netTotal = 0;
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            String item = jTable1.getValueAt(i, 0).toString();
            String qty = jTable1.getValueAt(i, 6).toString();
            String unit_price = jTable1.getValueAt(i, 8).toString();
            String unit = jTable1.getValueAt(i, 7).toString();
            String rcd = jTable1.getValueAt(i, 4).toString();
            String selling_price = jTable1.getValueAt(i, 9).toString();

            String expireDate = jTable1.getValueAt(i, 5).toString();

            netTotal = netTotal + (Double.parseDouble(qty) * Double.parseDouble(unit_price));
            try {
                db.iud("INSERT INTO grnitem (item_iditem,grn_idgrn,qty,unit_price,unit,returning_container_details)VALUES('" + item + "','" + grn + "','" + qty + "','" + unit_price + "','" + unit + "','" + rcd + "')");
                // retrieving the inserted grnitem id
                int idgrnitem = 0;
                ResultSet search = db.search("SELECT MAX(idgrnitem) FROM grnitem");
                if (search.next()) {
                    idgrnitem = search.getInt(1);
                }

                db.iud("INSERT INTO stock (item_iditem,qty,selling_price,expiredate,grnitem_idgrnitem)VALUES('" + item + "','" + qty + "','" + selling_price + "','" + expireDate + "','" + idgrnitem + "')");

            } catch (Exception e) {
                ErrorReporter.reportError(e, this, ErrorReporter.GENERIC_ERROR_MESSAGE);
            }
        }

        LoggingAndFeedbackHelper.successfulInsert("GRN " + grn + " was added", this);

        // do not take this variable below the clear fields method calling. Because then the grn total field is set to 0 and this 0 value is taken and passed to the grn report.
        String grn_total = grn_total_text.getText();

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        clearFields();
        clearMonetaryFields();
        clearSupplierFields();
        setData();
        // report generation
        try {

            Map<String, Object> m = new HashMap();
            m.put("idgrn", Integer.toString(grn));
            m.put("net_total", Double.toString(netTotal));
            m.put("grn_total", grn_total);
            m.put("balance", balance);

            String report = System.getenv("reports") + File.separator + "grn.jasper";

            JasperPrint fillReport = JasperFillManager.fillReport(report, m, db.getConnection());
            JasperViewer.viewReport(fillReport, false);
            JasperPrintManager.printReport(fillReport, true);
        } catch (Exception e) {
            ErrorReporter.reportError(e);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

