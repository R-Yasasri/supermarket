    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String amount = jTextField1.getText();
        String desc = jTextArea1.getText();
        Date date = jDateChooser1.getDate();

        if (Validator.isValidNumber(amount) && Validator.isValidString(desc) && date != null) {

            if (Double.parseDouble(amount) > 1000) {

                JOptionPane.showMessageDialog(this, "Petty cash amount should be lesser than Rs. 1000", "Error", JOptionPane.WARNING_MESSAGE);
                return;
            }

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

            try {
                String dateString = sdf.format(date);
                String type = jComboBox1.getSelectedItem().toString();

                db.iud("INSERT INTO petty_cash (type,amount,date,description)VALUES('" + type + "','" + amount + "','" + dateString + "','" + desc + "')");

                JOptionPane.showMessageDialog(this, "Successfully saved the details", "INFO", JOptionPane.INFORMATION_MESSAGE);

                clearFields();
                setId();
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {

            JOptionPane.showMessageDialog(this, "Invalid data", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


