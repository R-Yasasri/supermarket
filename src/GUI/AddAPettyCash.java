    private void setId() {

        try {
            ResultSet search = db.search("SELECT MAX(idpetty_cash) FROM petty_cash");

            int id = 0;
            if (search.next()) {

                id = search.getInt(1);
            }

            ++id;

            jLabel2.setText(Integer.toString(id));

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void clearFields() {
        jTextArea1.setText(null);
        jTextField1.setText(null);
        jDateChooser1.setDate(null);
    }
