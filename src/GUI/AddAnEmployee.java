    private void setId() {

        try {

            ResultSet search = db.search("SELECT MAX(idemployee) FROM employee");

            int id = 0;

            if (search.next()) {
                id = search.getInt(1);
            }

            if (id == 0) {
                id = 1;
            }

            jLabel2.setText(Integer.toString(id));
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

