    private void search() {
        String text = jTextField7.getText();

        if (!text.isEmpty()) {

            try {

                ResultSet search = db.search("SELECT * FROM employee WHERE idemployee='" + text + "'");

                if (search.next()) {

                    jLabel2.setText(text);

                    jTextField1.setText(search.getString("nic"));
                    jTextField2.setText(search.getString("fname"));
                    jTextField3.setText(search.getString("lname"));
                    jTextField4.setText(search.getString("email"));
                    jTextField5.setText(search.getString("mobile1"));
                    jTextField6.setText(search.getString("mobile2"));

                    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                    Date date = sdf.parse(search.getString("dob"));

                    jDateChooser1.setDate(date);

                    jTextArea1.setText(search.getString("address"));

                    String gender = search.getString("gender");

                    if (gender.equals("Male")) {
                        jRadioButton1.setSelected(true);
                    } else {
                        jRadioButton2.setSelected(true);
                    }

                    String civilStatus = search.getString("civil_status");

                    if (civilStatus.equals("Married")) {

                        jRadioButton4.setSelected(true);

                    } else {
                        jRadioButton3.setSelected(true);
                    }

                    //set image
                    imgPath = search.getString("img");

                    if (imgPath.isEmpty()) {
                        jLabel7.setText("no image");
                    } else {
                        File f = new File(imgPath);

                        Image img = ImageIO.read(f);
                        img = img.getScaledInstance(jLabel7.getWidth(), jLabel7.getHeight(), Image.SCALE_SMOOTH);
                        jLabel7.setText(null);
                        jLabel7.setIcon(new ImageIcon(img));

                    }

                    jPanel2.setVisible(true);
                } else {

                    JOptionPane.showMessageDialog(this, "No such Employee", "Warning", JOptionPane.WARNING_MESSAGE);

                }
            } catch (IOException io) {
                jPanel2.setVisible(true);
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }

    public void searchFromExternal(String empId) {
        jTextField7.setText(empId);
        search();
    }
