    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String amount = jTextField1.getText();
        String provider = jTextField2.getText();
        String paidBy = jTextField3.getText();
        String paymentCentre = jTextField4.getText();

        String type = jComboBox1.getSelectedItem().toString();
        String payment_method = jComboBox2.getSelectedItem().toString();

        String desc = jTextArea1.getText();
        String address = jTextArea2.getText();
        Date date = jDateChooser1.getDate();

        //validation
        if (!Validator.isValidNumber(amount)) {
            JOptionPane.showMessageDialog(this, "Invalid amount", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (provider.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill the a provider field", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (paidBy.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill the paidby field", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (paymentCentre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill the payment centre field", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (desc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please provide a description", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!Validator.isValidAddressString(address)) {
            JOptionPane.showMessageDialog(this, "Please provide a valid address", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (date == null) {
            JOptionPane.showMessageDialog(this, "Please provide a valid date", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // validation ends
        try {

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

            String dateString = sdf.format(date);

            db.iud("INSERT INTO bill (type,description,amount,date,paid_by,provider,payment_centre,payment_centre_address,payment_method) VALUES ('" + type + "','" + desc + "','" + amount + "','" + dateString + "','" + paidBy + "','" + provider + "','" + paymentCentre + "','" + address + "','" + payment_method + "')");

            JOptionPane.showMessageDialog(this, "Date Saved successfully", "INFO", JOptionPane.INFORMATION_MESSAGE);
            clearFields();
            setId();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


