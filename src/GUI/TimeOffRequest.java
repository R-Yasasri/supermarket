    private void setId() {

        try {
            ResultSet search = db.search("SELECT MAX(idtime_off_requests) FROM time_off_requests");

            int id = 0;
            if (search.next()) {
                id = search.getInt(1);
            }

            ++id;

            jLabel2.setText(Integer.toString(id));

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

