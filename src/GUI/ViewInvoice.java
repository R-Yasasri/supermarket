    private void search(String invoiceId) {

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        try {
            ResultSet invoiceSearch = db.search("SELECT * FROM invoice WHERE idinvoice='" + invoiceId + "'");

            if (invoiceSearch.next()) {

                jLabel12.setText(invoiceSearch.getString("discount"));
                jLabel13.setText(invoiceSearch.getString("payment_method"));
                jLabel11.setText(invoiceSearch.getString("payment"));
                jLabel3.setText(invoiceSearch.getString("idinvoice"));

                jLabel5.setText(invoiceSearch.getString("datetime"));
                // invoice item

                String query = "SELECT iditem,brand,name,qty,unit_price,unit,invitem.discount,(qty*unit_price-invitem.discount) AS total,invitem.stock_idstock FROM item it INNER JOIN invoiceitem invitem INNER JOIN invoice inv ON invitem.item_iditem=it.iditem AND inv.idinvoice=invitem.invoice_idinvoice WHERE inv.status=1 AND invitem.status=1 AND inv.idinvoice='" + invoiceId + "';";

                double netTotal = 0;

                ResultSet search = db.search(query);

                int count = 0;
                double total = 0;
                while (search.next()) {

                    ++count;

                    Vector v = new Vector();
                    v.add(count);
                    v.add(search.getString("iditem"));
                    v.add(search.getString("brand"));
                    v.add(search.getString("name"));
                    v.add(search.getString("qty"));
                    v.add(search.getString("unit_price"));
                    v.add(search.getString("unit"));
                    v.add(search.getString("discount"));
                    v.add(search.getString("total"));
                    v.add(search.getString("stock_idstock"));

                    total = total + search.getDouble("total");

                    dtm.addRow(v);
                }

                jLabel9.setText(Double.toString(total));

                showDetails(true);
                CustomLogging.loggingMethod("Invoice " + invoiceId + " was viewed", CustomLogging.INFO);

            } else {
                showDetails(false);
                JOptionPane.showMessageDialog(this, "No such Invoice", "Warning", JOptionPane.WARNING_MESSAGE);

            }

        } catch (Exception e) {
            ErrorReporter.reportError(e);
        }
    }

