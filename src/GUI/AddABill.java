    private void setId() {

        try {
            ResultSet search = db.search("SELECT MAX(idbill) FROM bill");

            int id = 0;
            if (search.next()) {
                id = search.getInt(1);
            }

            ++id;

            jLabel2.setText(Integer.toString(id));

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

