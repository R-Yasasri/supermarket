    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nic = jTextField1.getText();
        String fname = jTextField2.getText();
        String lname = jTextField3.getText();
        String email = jTextField4.getText();
        String mobile1 = jTextField5.getText();
        String mobile2 = jTextField6.getText();
        String address = jTextArea1.getText();
        Date date = jDateChooser1.getDate();
        String empId = jLabel2.getText();

        if (Validator.isValidStringMany(fname, lname) && Validator.isValidAddressString(address) && Validator.isValidPhoneNumber(mobile1) && Validator.isValidNIC(nic) && Validator.isValidEmail(email) && date != null) {

            if (!mobile2.isEmpty()) {

                if (!Validator.isValidPhoneNumber(mobile2)) {
                    JOptionPane.showMessageDialog(this, "Invalid data", "Warning", JOptionPane.WARNING_MESSAGE);
                    return;
                }

            }

            String gender = "Male";

            if (jRadioButton2.isSelected()) {
                gender = "Female";
            }

            String civilStatus = "Unmarried";
            if (jRadioButton4.isSelected()) {
                civilStatus = "Married";
            }

            try {

                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String bDay = sdf.format(date);

                db.iud("UPDATE employee SET nic='" + nic + "',fname='" + fname + "',lname='" + lname + "',img='" + imgPath + "',dob='" + bDay + "',gender='" + gender + "',mobile1='" + mobile1 + "',mobile2='" + mobile2 + "',address='" + address + "',civil_status='" + civilStatus + "' WHERE idemployee='" + empId + "'");

                JOptionPane.showMessageDialog(this, "Updated successfully", "INFO", JOptionPane.INFORMATION_MESSAGE);

                jPanel2.setVisible(false);
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {

            JOptionPane.showMessageDialog(this, "Invalid data", "WARNING", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

