    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String empId = jTextField1.getText();
        String description = jTextArea1.getText();
        Date date = jDateChooser1.getDate();

        //validation starts
        if (empId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please provide an employee id", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (description.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please provide a description", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (date == null) {
            JOptionPane.showMessageDialog(this, "Please provide a valid date", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // validate employee id
        try {
            ResultSet search = db.search("SELECT * FROM employee WHERE idemployee='" + empId + "'");

            if (search.next()) {
// nothing to do
            } else {
                JOptionPane.showMessageDialog(this, "Please provide a valid employee id", "Error", JOptionPane.WARNING_MESSAGE);
                return;
            }

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String dateString = sdf.format(date);

            db.iud("INSERT INTO performance_feedback (description,date,employee_idemployee) VALUES ('" + description + "','" + dateString + "','" + empId + "')");

            JOptionPane.showMessageDialog(this, "Data saved successfully", "INFO", JOptionPane.INFORMATION_MESSAGE);

            setId();
            clearFields();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


