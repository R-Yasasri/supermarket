    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String empId = jTextField1.getText();
        String salary = jTextField2.getText();
        Date date = jDateChooser1.getDate();

        //validation
        if (empId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please provide an employee id", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (salary.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please provide a salary amount", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (date == null) {
            JOptionPane.showMessageDialog(this, "Please provide a valid date", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!Validator.isValidNumber(salary)) {
            JOptionPane.showMessageDialog(this, "Please provide a valid salary amount", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        //validate employee id
        try {
            ResultSet search = db.search("SELECT * FROM employee WHERE idemployee='" + empId + "'");

            if (search.next()) {
                // ok. nothing to do    
            } else {
                JOptionPane.showMessageDialog(this, "Please provide a valid employee id", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            // validate salary (one salary per month)
            Calendar calendar = jDateChooser1.getCalendar();

            int year = calendar.get(Calendar.YEAR);
            int month = calendar.get(Calendar.MONTH) + 1;

            String salaryMonth = "";
            if (month < 10) {
                salaryMonth = year + "-0" + month;
            } else {
                salaryMonth = year + "-" + month;
            }

            String query = "SELECT month FROM salary WHERE employee_idemployee='" + empId + "' AND month LIKE '" + salaryMonth + "%'";
            ResultSet salarySearch = db.search(query);


            if (salarySearch.next()) {
                //the employee already received salary for the month

                String monthReceived = salarySearch.getString("month");

                JOptionPane.showMessageDialog(this, "The employee has already received the salary at " + monthReceived, "Error", JOptionPane.ERROR_MESSAGE);
                return;

            }

            // validation ends
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String dateString = sdf.format(date);

            db.iud("INSERT INTO salary (month,salary,status,employee_idemployee) VALUES ('" + dateString + "','" + salary + "',1,'" + empId + "')");

            JOptionPane.showMessageDialog(this, "Data saved successfully", "INFO", JOptionPane.INFORMATION_MESSAGE);

            setId();
            clearFields();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed


