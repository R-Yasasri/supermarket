    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String itemId = jTextField2.getText();
        String itemName = jLabel33.getText();
        String desc = jTextArea1.getText();
        Date exp = jDateChooser1.getDate();
        String qty = jTextField3.getText();
        String unit_price = jTextField4.getText();
        String selling_price = jTextField5.getText();
        String unit = jComboBox5.getSelectedItem().toString();

        // validation starts
        if (itemId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Provide an item id", "WARNING", JOptionPane.WARNING_MESSAGE);
            jTextField3.grabFocus();
            return;
        }
        if (itemName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid item name", "WARNING", JOptionPane.WARNING_MESSAGE);
            jTextField3.grabFocus();
            return;
        }

        if (!Validator.isValidNumber(qty)) {
            JOptionPane.showMessageDialog(this, "Invalid Qty", "WARNING", JOptionPane.WARNING_MESSAGE);
            jTextField3.grabFocus();
            return;
        }

        if (!Validator.isValidNumber(unit_price)) {
            JOptionPane.showMessageDialog(this, "Invalid Buying Price", "WARNING", JOptionPane.WARNING_MESSAGE);
            jTextField4.grabFocus();
            return;
        }

        if (!Validator.isValidNumber(selling_price)) {
            JOptionPane.showMessageDialog(this, "Invalid Selling Price", "WARNING", JOptionPane.WARNING_MESSAGE);
            jTextField5.grabFocus();
            return;
        }

        if (Double.parseDouble(selling_price) < Double.parseDouble(unit_price)) {
            JOptionPane.showMessageDialog(this, "Selling price can not be smaller than buying price", "WARNING", JOptionPane.WARNING_MESSAGE);
            jTextField5.grabFocus();
            return;
        }

        if (exp == null) {
            JOptionPane.showMessageDialog(this, "Invalid expire date", "WARNING", JOptionPane.WARNING_MESSAGE);
            jDateChooser1.grabFocus();
            return;
        }

        String date = "";

        try {
            SimpleDateFormat sdf = new SimpleDateFormat("YYYY-MM-dd");
            date = sdf.format(exp);
        } catch (Exception e) {
            ErrorReporter.reportError(e);
            JOptionPane.showMessageDialog(this, "Invalid expire date", "WARNING", JOptionPane.WARNING_MESSAGE);
            jDateChooser1.grabFocus();
            return;
        }

        Date today = new Date();
        if (exp.before(today)) {
            JOptionPane.showMessageDialog(this, "Expire date cannot be a previous day", "WARNING", JOptionPane.WARNING_MESSAGE);
            jDateChooser1.grabFocus();
            return;
        }

        // validation ends
        double total = Double.parseDouble(qty) * Double.parseDouble(unit_price);

        String id = jTextField2.getText();

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        Vector v = new Vector();

        v.add(id);
        v.add(jLabel31.getText());
        v.add(jLabel32.getText());
        v.add(jLabel33.getText());
        v.add(desc);
        v.add(date);
        v.add(qty);
        v.add(unit);
        v.add(unit_price);
        v.add(selling_price);
        v.add(total);

        dtm.addRow(v);
        clearFields();
        calculateNetTotal();

    }//GEN-LAST:event_jButton2ActionPerformed

