    public void loadAll() {

        try {
            ResultSet search = db.search("SELECT * FROM stock s INNER JOIN item i ON s.item_iditem=i.iditem WHERE s.status=2 AND s.qty>0");

            loadTable(search);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "An error occurred", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void loadTable(ResultSet search) {
        try {

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (search.next()) {

                Vector v = new Vector();

                double qty = search.getDouble("qty");
                double selling_price = search.getDouble("selling_price");

                double total = qty * selling_price;

                v.add(search.getString("idstock"));
                v.add(search.getString("iditem"));
                v.add(search.getString("category"));
                v.add(search.getString("brand"));
                v.add(search.getString("name"));
                v.add(Double.toString(qty));
                v.add(Double.toString(selling_price));
                v.add(Double.toString(total));
                v.add(search.getString("expiredate"));
                v.add(false);

                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "An error occurred", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void searchStock() {

        try {
            int selectedIndex = jComboBox1.getSelectedIndex();

            ResultSet search = null;

            if (selectedIndex == EXPIRE_DATE_SEARCH) {

                if (jDateChooser1.getCalendar() == null) {

                    JOptionPane.showMessageDialog(this, "Provide a valid date", "ERROR", JOptionPane.ERROR_MESSAGE);
                } else {
                    Calendar calendar = jDateChooser1.getCalendar();

                    int month = calendar.get(Calendar.MONTH) + 1;
                    String expireDate = calendar.get(Calendar.YEAR) + "-" + month + "-" + calendar.get(Calendar.DATE);

                    String query = "SELECT * FROM stock s INNER JOIN item i ON s.item_iditem=i.iditem WHERE s.expiredate='" + expireDate + "' AND s.qty>0 AND s.status=2";
                    search = db.search(query);

                }

            } else {
                String text = jTextField2.getText();

                if (text.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Empty search field", "ERROR", JOptionPane.ERROR_MESSAGE);
                } else {

                    if (selectedIndex == ID_SEARCH) {

                        search = db.search("SELECT * FROM stock s INNER JOIN item i ON s.item_iditem=i.iditem WHERE iditem = '" + text + "' AND s.qty>0 AND s.status=2");

                    } else if (selectedIndex == CATEGORY_SEARCH) {
                        search = db.search("SELECT * FROM stock s INNER JOIN item i ON s.item_iditem=i.iditem WHERE category LIKE '%" + text + "%' AND s.qty>0 AND s.status=2");
                    } else if (selectedIndex == BRAND_SEARCH) {
                        search = db.search("SELECT * FROM stock s INNER JOIN item i ON s.item_iditem=i.iditem WHERE brand LIKE '%" + text + "%' AND s.qty>0 AND s.status=2");
                    } else {
                        search = db.search("SELECT * FROM stock s INNER JOIN item i ON s.item_iditem=i.iditem WHERE name LIKE '%" + text + "%' AND s.qty>0 AND s.status=2");
                    }

                }
            }

            if (search != null) {
                loadTable(search);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "An error occurred", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }
