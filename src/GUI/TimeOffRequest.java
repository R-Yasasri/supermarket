    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String reason = jTextArea1.getText();
        Date date = jDateChooser1.getDate();
        String empId = jTextField1.getText();
        String startTime = jTextField2.getText();
        String endTime = jTextField3.getText();

        //validation starts
        if (empId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please provide an employee id", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (reason.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please provide a reason", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (date == null) {
            JOptionPane.showMessageDialog(this, "Please provide a valid date", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // validate start time
        SimpleDateFormat sdf = new SimpleDateFormat("hh:mm");

        try {

            Date s = sdf.parse(startTime);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please provide the start time in valid format", "Error", JOptionPane.ERROR_MESSAGE);
            return;

        }

        try {

            Date s = sdf.parse(endTime);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please provide the end time in valid format", "Error", JOptionPane.ERROR_MESSAGE);
            return;

        }

        // validate the employee id
        try {
            ResultSet empSearch = db.search("SELECT * FROM employee WHERE idemployee='" + empId + "'");

            if (empSearch.next()) {
                // nothing to do. valid
            } else {
                JOptionPane.showMessageDialog(this, "Please provide a valid employee id", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // validation ends
            SimpleDateFormat dateSdf = new SimpleDateFormat("yyyy-MM-dd");
            String format = dateSdf.format(date);

            String sTime = format + " " + startTime;
            String eTime = format + " " + endTime;

            db.iud("INSERT INTO time_off_requests (reason,begin_time,end_time,employee_idemployee) VALUES ('" + reason + "','" + sTime + "','" + eTime + "','" + empId + "')");

            JOptionPane.showMessageDialog(this, "Data saved successfully", "INFO", JOptionPane.INFORMATION_MESSAGE);
            clearFields();
            setId();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error occurred. Please recheck the data", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


